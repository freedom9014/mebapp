package comic;

public class ComicProperty {
    private String comicPublisher;
    private String comicTitle;
    private String comicAuthor;

    public String getComicPublisher(){
        return comicPublisher;
    }

    public void setComicPublisher(String _comicPublisher){
        comicPublisher = _comicPublisher;
    }

    public String getComicTitle(){
        return comicTitle;
    }

    public void setComicTitle(String _comicTitle){
        comicTitle = _comicTitle;
    }

    public String getComicAuthor(){
        return comicAuthor;
    }

    public void setComicAuthor(String _comicAuthor){
        comicAuthor = _comicAuthor;
    }
    
}
